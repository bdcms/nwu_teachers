			$('#district_metro').keyup(function(){
				//console.log('check');
				var area = $(this).val();
				if(area != ''){
					$.ajax({
						url:"{{ url('/area_search') }}",
						method:"POST",
						data:{area:area, _token:'{{csrf_token()}}'},
						success:function(data){
							$('#areatatus').fadeIn();
							$('#areatatus').html(data);
							//console.log(data);
						}
					});
				}
			$(document).on('click','li',function(){
				$('#district_metro').val($(this).text());
				$('#areatatus').fadeOut();
			});
			});


			$('#keyword').keyup(function(){
				//console.log('check');
				var keyword = $(this).val();
				if(keyword != ''){
					$.ajax({
						url:"{{ url('/key_search') }}",
						method:"POST",
						data:{keyword:keyword, _token:'{{csrf_token()}}'},
						success:function(data){
							$('#keystatus').fadeIn();
							$('#keystatus').html(data);
							//console.log(data);
						}
					});
				}
			$(document).on('click','span',function(){
				$('#keyword').val($(this).closest("div").text());
				$('#keystatus').fadeOut();
			});  
			});

